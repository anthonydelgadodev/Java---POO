/* Encapsulacion */

/* 
La encapsulación es uno de los conceptos fundamentales en la programación orientada a 
objetos y se refiere a la práctica de ocultar los detalles internos de una clase y proporcionar
una interfaz controlada y bien definida para interactuar con dicha clase. En Java, la 
encapsulación se logra mediante el uso de modificadores de acceso y el concepto de 
métodos getter y setter.
*/


