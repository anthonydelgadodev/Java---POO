/* POP : Programacion orientada a procedimientos */
// Algunos ejemplos de lenguajes: Fortran, Cobol, Basic etc.
/* Desventajas: */
// - Unidades de codigo muy grandes en aplicaciones complejas.
// - En aplicaciones complejas el codigo resultaba dificil de descifrar.
// - Poco reutilizable
// - Si existe fallo en alguna linea del codigo, es muy probable que el programa caiga.
// - Aparicion frecuente de codigo espagueti.
// - Dificil de depurar por otro programadores en caso de necesidad o error.



/* POO : Programacion orientada a objetos */

/* En que consiste? */
// Trasladar la naturaleza de los objetos de la vida real al codigo de programacion.

/* Cual es la naturaleza de un objeto de la vida real? */
// Los objetos tienen un estado, comportamiento (¿Que puede hacer?), y unas propiedades

/* Ejemplo: El objeto coche */
// ¿Cual es el estado de un coche? Un coche puede estar parado, circulando, aparacado etc.
// ¿Que propiedades tiene un coche? Un coche tiene un color, un peso, un tamaño etc.
// ¿Que comportamiento tiene un coche? Un coche puede arrancar, frenar, acelerar, girar etc.

/* Algunos ejemplos de lenguajes: C+, Java, Visual .NET etc.  */
/* Ventajas: */
// - Programas dividos en "trozos", "partes", "modulos" o "clases". Modularizacion
// - Muy reutilizable. Herencia
// - Si existe fallo en alguna linea del codigo, el programa continuara con su funcionamiento. Tratamiento de Excepciones.
// - Encapsulamiento.



