/* Modularizacion */


/*
La modularización en el contexto de la programación orientada a objetos (POO) en Java 
se refiere a la práctica de dividir un programa en módulos o componentes más pequeños y 
autónomos. Cada módulo realiza una tarea específica y está diseñado para interactuar con 
otros módulos de manera cohesiva y acoplada. La modularización busca mejorar la 
legibilidad, la mantenibilidad y la reutilización del código, al tiempo que reduce la 
complejidad general del sistema.
*/



//En Java, la modularización se puede lograr de varias formas, como:

/*
#1)- Clases y Objetos: Las clases son las unidades básicas de modularización en Java. Cada
clase define una estructura de datos y el comportamiento asociado. Los objetos son 
instancias de estas clases y encapsulan los datos y el comportamiento.
*/
/*
#2)- Encapsulación: Es el principio de ocultar la implementación interna de una clase y 
proporcionar una interfaz pública bien definida para interactuar con ella. Esto permite
cambiar la implementación interna sin afectar el código que utiliza la clase.
*/
/*
#3)- Abstracción: Implica identificar las características esenciales de un objeto y modelarlas 
como clases abstractas o interfaces. Esto ayuda a simplificar la complejidad al centrarse en 
lo esencial.
*/
/* 
#4)- Herencia: Permite crear nuevas clases basadas en clases existentes, heredando sus
Permite crear nuevas clases basadas en clases existentes, heredando sus
jerarquías de clases.
*/
/*
#5)- Polimorfismo: Permite tratar objetos de diferentes clases de manera uniforme a través de 
Permite tratar objetos de diferentes clases de manera uniforme a través de 
múltiples tipos de objetos de manera coherente.
*/
/*
#6)- Paquetes: Los paquetes son contenedores que agrupan clases relacionadas. Proporcionan
un mecanismo para organizar y separar las clases en unidades lógicas, lo que mejora la
claridad y la estructura del proyecto.
*/
/* 
#7)- Módulos (Java 9 en adelante): Java 9 introdujo el concepto de módulos a nivel de lenguaje. 
Los módulos permiten definir y declarar dependencias explícitas entre componentes y 
controlar qué partes del código son accesibles desde otras partes. Esto mejora la 
modularidad y facilita el desarrollo en equipos grandes.
*/


/* 
la modularización en la programación orientada a objetos en Java se trata de 
dividir un programa en partes más pequeñas y coherentes para simplificar el diseño, la 
implementación y el mantenimiento del código.
*/