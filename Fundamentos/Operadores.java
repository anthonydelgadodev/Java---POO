/* Operadores en Java */

/* Aritmeticos */
// + : suma
// - : resta
// * : multiplicacion
// / : division

/* Logicos, relacionales y booleanos */
// > : mayor que
// < : menor que
// <> : mayor o menor que
// != : distinto que
// == : igual que
// && : AND (Y)

/* Incremento y decremento */
// ++ : Incremento
// -- : Decremento
// += n°: Incremento
// -= n°: Decremento

/* Concatenacion */
// + : une o concatena


public class Operadores {

    public static void main(String[] args) {

        int a = 5;

        int b;
        b = 7;

        int c = b + a;

        c++; // Incremento +1 a la variable

        System.out.println(c);
    }
}